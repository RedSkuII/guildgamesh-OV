// Multiplier based on type and rarity
function getMultiplier(name, type) {
  const lowerName = name.toLowerCase()
  
  // Special high-value items
  if (lowerName.includes('spice') && lowerName.includes('melange')) return 3.0
  if (lowerName.includes('spice-infused')) return 2.5
  if (lowerName.includes('plastanium')) return 2.2
  if (lowerName.includes('plasteel')) return 2.0
  if (lowerName.includes('duraluminum')) return 1.8
  
  // Advanced/improved items
  if (lowerName.includes('advanced') || lowerName.includes('improved')) return 1.7
  if (lowerName.includes('military') || lowerName.includes('precision')) return 1.8
  if (lowerName.includes('overclocked') || lowerName.includes('tri-forged')) return 1.9
  
  // Refined materials
  if (type === 'Refined') return 1.5
  
  // Components
  if (type === 'Component') return 1.4
  
  // Raw materials
  if (type === 'Raw') return 1.0
  
  // Currency
  if (type === 'Currency') return 1.0
  
  return 1.2
}

// Map game types to categories
function mapCategory(type) {
  const categoryMap = {
    'Raw': 'Raw Materials',
    'Refined': 'Refined Materials',
    'Component': 'Components',
    'Currency': 'Currency'
  }
  return categoryMap[type] || 'Materials'
}

lines.forEach((line) => {
  // Split by tabs
  const parts = line.split('\t')
  if (parts.length !== 3) return
  
  // First part has the name duplicated, extract just once
  const namePart = parts[0]
  // Find where the duplicate starts (capital letter after lowercase)
  const nameMatch = namePart.match(/^([A-Z][a-z]*(?:\s+[A-Z][a-z]*)*(?:\s+[A-Z][a-z]*)*(?:-[A-Z][a-z]*)*(?:-[a-z]*)*)/);
  const name = nameMatch ? nameMatch[1].trim() : namePart.split(/(?=[A-Z][a-z])/)[0].trim()
  
  const type = parts[1].trim()
  const description = parts[2].trim()
  
  // Skip if we couldn't parse properly
  if (!name || !type || !description) return
  
  const icon = getIcon(name, type)
  const multiplier = getMultiplier(name, type)
  const category = mapCategory(type)
  
  resources.push({
    name,
    category,
    description,
    icon,
    multiplier
  })
})

